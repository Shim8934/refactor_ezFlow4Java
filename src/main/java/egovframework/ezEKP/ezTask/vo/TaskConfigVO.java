package egovframework.ezEKP.ezTask.vo;

public class TaskConfigVO {
	/** userID */
	private String userID;
	/** DelayColor */
	private String delayColor;
	/** CompleteColor */
	private String completeColor;
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getDelayColor() {
		return delayColor;
	}
	public void setDelayColor(String delayColor) {
		this.delayColor = delayColor;
	}
	public String getCompleteColor() {
		return completeColor;
	}
	public void setCompleteColor(String completeColor) {
		this.completeColor = completeColor;
	}
}
