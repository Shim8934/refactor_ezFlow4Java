package egovframework.ezEKP.ezApprovalG.vo;

public class ApprGOpenGovInfoVO {
	private String docTitle;
	private String writerName;
	private String writerDeptName;
	private String docNo;
	private String publicityCode;
	private String listOpenFlag;
	private String openLimitDate;
	private String reason;
	private String basis;
	
	public String getDocTitle() {
		return docTitle;
	}
	public void setDocTitle(String docTitle) {
		this.docTitle = docTitle;
	}
	public String getWriterName() {
		return writerName;
	}
	public void setWriterName(String writerName) {
		this.writerName = writerName;
	}
	public String getWriterDeptName() {
		return writerDeptName;
	}
	public void setWriterDeptName(String writerDeptName) {
		this.writerDeptName = writerDeptName;
	}
	public String getDocNo() {
		return docNo;
	}
	public void setDocNo(String docNo) {
		this.docNo = docNo;
	}
	
	public String getPublicityCode() {
		return publicityCode;
	}
	public void setPublicityCode(String publicityCode) {
		this.publicityCode = publicityCode;
	}
	public String getListOpenFlag() {
		return listOpenFlag;
	}
	public void setListOpenFlag(String listOpenFlag) {
		this.listOpenFlag = listOpenFlag;
	}
	public String getOpenLimitDate() {
		return openLimitDate;
	}
	public void setOpenLimitDate(String openLimitDate) {
		this.openLimitDate = openLimitDate;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public String getBasis() {
		return basis;
	}
	public void setBasis(String basis) {
		this.basis = basis;
	}
	
	
}
