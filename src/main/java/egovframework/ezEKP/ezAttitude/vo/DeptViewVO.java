package egovframework.ezEKP.ezAttitude.vo;

public class DeptViewVO {
	   private String id;
	   private String parent;
	   private String companyId;
	   private String text;
	   private String isComp;
	   private String myDept;
	   public String getId() {
	      return id;
	   }
	   public void setId(String id) {
	      this.id = id;
	   }
	   public String getParent() {
	      return parent;
	   }
	   public void setParent(String parent) {
	      this.parent = parent;
	   }
	   public String getCompanyId() {
	      return companyId;
	   }
	   public void setCompanyId(String companyId) {
	      this.companyId = companyId;
	   }
	   public String getText() {
	      return text;
	   }
	   public void setText(String text) {
	      this.text = text;
	   }
	   public String getIsComp() {
	      return isComp;
	   }
	   public void setIsComp(String isComp) {
	      this.isComp = isComp;
	   }
	   public String getMyDept() {
	      return myDept;
	   }
	   public void setMyDept(String myDept) {
	      this.myDept = myDept;
	   }
}
